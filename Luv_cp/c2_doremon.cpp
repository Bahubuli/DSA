const int N = 1e5+10;
int arr[N];

int main()
{

        // freopen("i.txt", "r", stdin);
        // freopen("o.txt", "w", stdout);

        int m, n;
        cin >> m >> n;

        while(m--)
        {
            int k, d;
            cin >> k >> d;
            arr[d]==1; //  = ==

        }

        string s = "YES";
        while(n--)
        {
            int q;
            cin >> q;

            if(arr[q]==0)
            {
                s = "NO";
                break;
            }
        }
        cout << s ;
}
